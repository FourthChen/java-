package cn.itcast.day05.demo03;

public class demo04Aarray {
    public static void main(String[] args) {
        int []  array={1,2,4,5,7};

        int len=array.length;
        for (int i = 0; i < len; i++) {
            System.out.println(array[i]);
        }
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
