package cn.itcast.day10.demo01;
/*
* 在任何版本的java中，都可以定义抽象方法
*
* public abstract 返回值类型 方法名称(参数列表)
* */
public interface MyInterfaceAbstract {
    public abstract void methodAbs();
    //也是抽象方法
    abstract void methodAbs1();
    //也是抽象方法
    public void methodAbs2();
    //也是抽象方法
    void methodAbs3();
}
